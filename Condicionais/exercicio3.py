
#3. DDD:

# Blumenau - DDD 47
# Florianópolis - DDD 48
# Concórdia - DDD 49
# São Paulo - DDD 11
# Rio de Janeiro - DDD 21

ddd = int(input("Insira o seu DDD: "))

if(ddd == 47):
    print("Blumenau")

elif(ddd == 48):
    print("Florianopolis")

elif(ddd == 49):
    print("Concórdia")

elif(ddd == 11):
    print("São Paulo")

elif(ddd == 21):
    print("Rio de Janeiro")

else:
    print("Inválido")