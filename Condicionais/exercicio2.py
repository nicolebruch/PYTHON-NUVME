#2. Compra na Cantina:


# 1 - Cachorro quente: R$10
# 2 - Pastel: R$6
# 3 - Coxinha: R$5
# 4 - Bala: R$1

comida = str(input("Insira a comida para consultar o valor: ".lower()))

if (comida == "Cachorro Quente"):
    print("O valor é R$10")

elif (comida == "Pastel"):
    print("O valor é R$6")

elif (comida == "Coxinha"):
    print("O valor é R$5")

elif (comida == "Bala"):
    print("O valor é R$1")

else:
    print("Nada")
    